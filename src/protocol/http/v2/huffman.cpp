auto huffman_decode(std::span<const std::byte> input) -> std::expected<std::string, std::error_code> {
    std::string result;
    std::uint32_t code = 0;
    unsigned length = 0;
    for (const auto byte : input) {
        for (int shift = 7; shift >= 0; --shift) {
            code = (code << 1U) | ((std::to_integer<std::uint8_t>(byte) >> shift) & 1U);
            ++length;
            bool matched = false;
            for (std::size_t index = 0; index < table.size(); ++index) {
                const auto item = table[index];
                if (item.length != length || item.bits != code) continue;
                if (index == 256) return std::unexpected(std::make_error_code(std::errc::protocol_error));
                result.push_back(static_cast<char>(index));
                code = 0;
                length = 0;
                matched = true;
                break;
            }
            if (!matched && length == 30)
                return std::unexpected(std::make_error_code(std::errc::protocol_error));
        }
    }
    // RFC 7541 ?5.2: padding is at most seven one bits and must be an EOS prefix.
    if (length != 0 && (length > 7 || code != ((1U << length) - 1U)))
        return std::unexpected(std::make_error_code(std::errc::protocol_error));
    return result;
}

auto huffman_encode(std::string_view input) -> std::vector<std::byte> {
    std::vector<std::byte> result;
    std::uint64_t bits = 0;
    unsigned length = 0;
    for (const auto character : input) {
        const auto item = table[static_cast<unsigned char>(character)];
        bits = (bits << item.length) | item.bits;
        length += item.length;
        while (length >= 8) {
            length -= 8;
            result.push_back(static_cast<std::byte>(bits >> length));
        }
    }
    if (length != 0)
        result.push_back(static_cast<std::byte>((bits << (8 - length)) | ((1U << (8 - length)) - 1U)));
    return result;
}
